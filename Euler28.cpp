//1001 X 1001 螺旋矩阵
#include <stdio.h>

int main() {
    int sum = 0;
    for (int i = 3; i < 1002; i += 2) {

        sum += i * i * 4 - 6 * i + 6;
    }
    sum += 1;
    printf("%d\n", sum);

    return 0;
}
